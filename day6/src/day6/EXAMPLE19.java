package day6;

public class EXAMPLE19 {

		public static void main(String[] args) 
		{

		  // for loop
		  for(int i=1,j=1; i<10||j<10;i++,j++)
		  { 
			  j++;
			  System.out.println("i="+i+" and j="+j); 
		  }


		  // while loop 
			  int i=1,j=1;
			  while(i<10 || j<10)
			  { 
				  i++; j++;
			      System.out.println("i="+i+" and j="+j); 
			  }	
			
		// Do while loop
			int i1 = 1, j1 = 1;
			do {
				i1++;
				j1++;
				System.out.println("i1=" + i + " and j1=" + j);
			    } while (i1 < 10 || j1 < 10);
		}
	}



		 
	


